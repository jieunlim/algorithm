# 843. Guess the Word

# [input]: a word list of unique words, 6 letters long.
# one word in this list is chosen as secret
# secret = "acckzz", wordlist = ["acckzz","ccbazz","eiowzz","abcczz"]
# You may call master.guess(word), if word is "abcczz", master.guess will return 4.
# The function return represents the number of exact matches(value and position) of your guess to the secret word.
# if your guess is not in the wordlist, returns -1.
# if you have made 10 or less calls to master.guess and at least one of these guesses was the secret,
# you pass the testcase

# secret = "abcczz"
# wordlist = ["acckzz","ccbazz","eiowzz","abcczz"]
# 주어진 입력,
# master.guess(내부구현되어있다고치고) 에 wordlist 중에 유력한 후보를 찾아서 10번 이내 secret word를 맞추는 문제.

# 1) wordlist 처음부터 끝까지 다 던져봄 O(n) -> 10번 안에 secret 을 맞출 수 없음
# 2) shuffle or random 하게 던져봄 O(n) -> 운애 맡겨야 함.
# ==> better solution
# 3) count the occurrence of characters
# wordlist 중에서 가장 중복되는 letters가 많은 단어를 골라서 master.guess에 던져보고 받은 return number를 이용해서 다음 후보 선택.
# 즉, abcczz가 secret라고 치고,
# wordlist중 가장 중복된 letter가 많은 acckzz(첫번째 element)를 후보로 고른 후 master.guess에 던져보 4 가 리턴.
# wordlist에서 acckzz 와 비교해서 4 인 후보를 골라서 다시 master.guess 에 던져봄.
# 10회 이내에 맞춰야하므로, 최대한 확률이 높은 후보를 뽑아서 guess에 던지는 것이 중요.
# 10회 이내가 가능함을 증명하기는 쉽지 않을 듯.

LETTER_LEN = 6

class Master:
    def __init__(self, secretWord, wordList):
        self.testCnt = 0
        self.secret = secretWord
        self.wordList = wordList

    def guess(self, word: str) -> int:

        if self.testCnt > 10:
            print(f"You've already had 10 guesses. self.testCnt={self.testCnt}")
            return -2

        self.testCnt += 1

        if word not in self.wordList:
            return -1

        cnt = 0
        for i in range(len(word)):
            if word[i] == self.secret[i]:
                cnt += 1
        print(f"[Master.guess] word={word}, self.secret={self.secret}, cnt={cnt}, self.testCnt={self.testCnt}")

        if cnt == LETTER_LEN:
            print("You guessed the secret word correctly.")

        return cnt

